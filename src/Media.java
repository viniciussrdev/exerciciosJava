public class Media {
    public static void main(String[] args) {
        double nota1 = 8.9;  // Define a primeira nota
        double nota2 = 9.5;  // Define a segunda nota
        double media = (nota1 + nota2) / 2;  // Calcula a média das notas

        System.out.println("Média: " + media);  // Exibe a média
    }
}