public class Palavras {
    public static void main(String[] args) {

        char letra = 'A';  // Define uma letra
        String palavra = "MARELO";  // Define uma palavra

        String mensagem = "A letra é " + letra + " e a palavra é " + palavra;  // Mensagem com a letra e a palavra

        System.out.println(mensagem);  // Exibe a mensagem
    }
}